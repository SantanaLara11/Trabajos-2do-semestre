numero=int(input("Dime tu numero: "))

if numero>=1:
    numero2=str(numero)
    if len(numero2)==1:
       print(f"Tu numero {numero2} es de un digito")
    elif len(numero2)==2:
        print(f"Tu numero {numero2} es de dos digitos")
    elif len(numero2)==3:
        print(f"Tu numero {numero2} es de tres digitos")
    elif len(numero2)>=4:
        print(f"Tu numero {numero2} es erroneo")
        
    
        
    
        
    
        

    